let x = 5; // 0101 in Binär
let y = 3; // 0011 in Binär

console.log(x & y); // 1 (0001 in Binär)
console.log(x | y); // 7 (0111 in Binär)
console.log(x ^ y); // 6 (0110 in Binär)
console.log(~x); // -6 (invertiert alle Bits)
console.log(x << 1); // 10 (1010 in Binär)
console.log(x >> 1); // 2 (0010 in Binär)