const fruit = 'Banane';

switch (fruit) {
  case 'Apfel':
    console.log('Äpfel sind rot oder grün.');
    break;
  case 'Banane':
    console.log('Bananen sind gelb.');
    break;
  case 'Kirsche':
    console.log('Kirschen sind rot.');
    break;
  default:
    console.log('Unbekannte Frucht.');
}